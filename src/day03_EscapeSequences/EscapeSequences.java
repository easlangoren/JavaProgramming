package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote, yani " " bunun araliginda olmali escape sequences.
        \n      starts a the new line
        \t      tab button
        \\      single back slash
        \"      double quote

 */

public class EscapeSequences {
    public static void main(String[] args) {


        System.out.println("Java \nPython  \nC#");

        System.out.println("---------------------------------------------------------------");

        System.out.println("Hello Cydeo\nHow are you all today?\nIt's nice to see you all! What class do we have next week?");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("---------------------------------------------------------------");

        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("---------------------------------------------------------------");

        System.out.println("My favorite TV show is \"Game of Throne\" "); // in order to print double quote we need to use \" sequence











    }












}
